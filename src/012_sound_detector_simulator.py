# Sound Detector Simulator
# 파일 설명: 이 시뮬레이션은 고양이의 예민한 청각을 테마로 합니다.
# 주인님 주변에서 들려오는 미세한 소리(나뭇가지 밟는 소리, 낯선 발소리, 새의 지저귐 등)의 출처, 방향, 종류를 파악하고 추적하는 과정을 텍스트 기반으로 구현합니다.
# 목표: 단순히 소리를 듣는 것을 넘어, 소리 뒤에 숨겨진 '사건'이나 '존재'를 추리하는 고차원적인 상호작용 경험을 제공합니다.
# 핵심 메커니즘: 소리가 발생 -> 주인님(사용자)이 추리 -> 소리의 출처를 특정 -> 다음 시나리오가 전개.
# 사용 예시: "저기 부엌 쪽에서 작은 '톡' 소리가 들린다. 그 소리는 무엇일까?" 등의 힌트를 통해 주인님의 관찰력을 테스트합니다.