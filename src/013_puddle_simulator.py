# Puddle/Pool Explorer Simulator
# 파일 설명: 이 시뮬레이션은 고양이에게 매우 중요한 '물'과 '경계선'에 대한 본능을 구현합니다.
# 주인님 주변의 작은 웅덩이(Puddle)나 어항, 심지어 세탁실 바닥의 물웅덩이 등 물과 관련된 환경을 탐험하는 게임입니다.
# 목표: 물의 온도, 깊이, 움직이는 사물(나뭇잎, 반사된 빛 등)을 관찰하며 본능적인 호기심과 놀이 본능을 자극하는 상호작용 경험을 제공합니다.
# 핵심 메커니즘: 웅덩이를 발견 -> 탐험 단계(조심스럽게 맛보기, 물장구치기, 구석구석 보기) -> 물속의 재미있는 '발견물' 찾기.
# 사용 예시: "저기에 신기한 물웅덩이가 생겼다! 이 물은 어떨까?" 같은 힌트를 통해 주인님의 관찰력과 물놀이 본능을 테스트합니다.