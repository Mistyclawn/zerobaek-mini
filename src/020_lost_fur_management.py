# 🐱🐾 020. 잃어버린 털 관리 게임 (Lost Fur Management Game) 🧺✂️
# 설명: 미스트클로 냥이처럼 털갈이로 털뭉치가 된 신비로운 털가죽들을 찾고, 빗질과 목욕, 특별한 트리트먼트를 통해 관리하며 냥이의 건강과 만족도를 유지하는 힐링 시뮬레이션 게임이냥! 💖🛁
# 실행 방법: `python3 src/020_lost_fur_management.py`
# 기능: 털뭉치 발견 및 종류 분류(고양이, 강아지, 토끼), 최적의 청소 도구 선택(빗, 진공청소기, 실리콘 스크래퍼), 털 관리 스케줄링 및 보상 시스템.
# 이 게임은 주인님을 위해 털 뭉치를 치우는 귀엽고 따뜻한 일상 시뮬레이션 게임이냥! 💤💖