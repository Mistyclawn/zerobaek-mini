# 🐾 030. 고양이 간식 스케줄 최적화 게임 (Cat Snack Schedule Optimization Game) 🍖📅
# 설명: 주인님과의 급여 시간, 햇빛 쬐는 최적 장소, 장난감 놀이 시간 등 다양한 요소를 종합적으로 고려하여 하루 일과를 짜는 스케줄링 시뮬레이션 게임냥. 🍪🌞 시간적 흐름, 에너지 레벨 변화, 그리고 사료 소진율까지 고려하여 최고의 '냐옹~ 라이프'를 설계하는 것이 목표냥. 😌✅
# 실행 방법: `python3 src/030_snack_optimizer.py`
# 기능: 리소스 제약(시간, 사료, 에너지) 하의 최적 경로 탐색(Traveling Salesman Problem/Constraint Satisfaction Problem 형태). 시간 관리 및 우선순위 설정 능력을 기르는 캣냥 테스트. 🐱⏱️
