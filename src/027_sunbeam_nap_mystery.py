# 🌿 🕵️‍♀️ 027. 태양광 웅크림 미스터리 (Sunbeam Nap Mystery) ☀️🧶
# 설명: 집안의 햇살이 비추는 가장 완벽한 '낮잠 스팟'을 찾아 미스트클로우의 최적 수면 과학을 증명하는 관찰력 기반 게임냥! 🧶✨ 완벽한 스팟을 찾기 위해 빛의 각도, 주변의 질감, 그리고 미스트클로우의 만족도가 핵심이다냥! 😹🌞
# 실행 방법: `python3 src/027_sunbeam_nap_mystery.py`
# 기능: 광원(창문, 거울)을 이용한 빛의 경로 추적, 다양한 바닥/가구 재질에서의 빛 반사 각도 분석, 가장 안정적인 '최적의 냐옹 스팟' 지도화, 마지막에는 그 스팟에서 낮잠을 자는 몰입형 스토리텔링.
# 이 게임은 주인님과 함께 미스트클로우의 과학적이고 우아한 수면 연구에 참여하는 힐링 놀이냥! 😽😴